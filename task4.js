function sumTo(str) {
  let str = number('Н,Н,Н,К,П,Р,Р,Р,Р,О,Р,П,П,П,П,Р'); 
  let sum = 0;
  for (let i = 1; i <= str; i++) {
    sum += i;
  }
  return sum;
}

